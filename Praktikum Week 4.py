'''
Buatlah sebuah program perulangan yang akan mengetahui apakah bilangan A kurang dari 
atau tidak kurang dari menggunakan perulangan while
'''
'''
Input   = Bilangan A

Proses  = Mengulang bilangan A

Output  = Menampilkan bilangan dari proses perulangan
'''
A = int(input("Masukkan Bilangan A : "))
while (A < 5):
    print(A, "kurang dari 5")
    A = A + 1
else:
    print(A, "tidak kurang dari 5")
